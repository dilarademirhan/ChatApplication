    private void btn_sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sendActionPerformed

        String message = txt_message.getText();
        String sendTo = lbl_chatWith.getText();
        for (cClient c : cClient.clients) {
            if (c.name.equals(lbl_thisclient_name.getText())) {
                Message m = new Message(Message.Message_Type.Dm);
                m.additional = sendTo + " " + c.name;
                m.content = message;
                c.SendMessage(m);
            }
        }
        String s = "<html><b>" + lbl_thisclient_name.getText() + ":" + message + "</b></html>";
        messagesModel.addElement(s);
        txt_message.setText("");
    }//GEN-LAST:event_btn_sendActionPerformed

    private void btn_sendFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sendFileActionPerformed
        File file = fileChooser.getSelectedFile();
        byte[] bytes = cClient.writeFile(file);
        Message m = new Message(Message.Message_Type.File);
        m.additional = "dm " + lbl_chatWith.getText() + " " + lbl_thisclient_name.getText() + " " + file.getName();
        m.content = bytes;
        for (cClient c : cClient.clients) {
            if (c.name.equals(lbl_thisclient_name.getText())) {
                c.SendMessage(m);
            }
        }
        String s = "<html><b>" + lbl_thisclient_name.getText() + ":" + file.getName() + "</b></html>";
        messagesModel.addElement(s);
        frmFile.setVisible(false);

    }//GEN-LAST:event_btn_sendFileActionPerformed

