    private void btn_sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sendActionPerformed
        for (Room r : Room.Rooms) {
            if (r.name.equals(lbl_roomName.getText())) {
                for (cClient c : r.clientsOfRoom) {
                    if (c.name.equals(txt_thisclient_name.getText())) {
                        Message msg = new Message(Message.Message_Type.RMessage);
                        msg.additional = lbl_roomName.getText() + " " + c.name;
                        msg.content = txt_message.getText();
                        c.SendMessage(msg);
                    }
                }
            }
        }
        txt_message.setText("");
    }//GEN-LAST:event_btn_sendActionPerformed

    private void btn_sendFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sendFileActionPerformed
        File file = fileChooser.getSelectedFile();
        byte[] bytes = cClient.writeFile(file);
        Message m = new Message(Message.Message_Type.File);
        m.additional = "room " + lbl_roomName.getText() + " " + txt_thisclient_name.getText() + " " + file.getName();
        m.content = bytes;
        for (cClient c : cClient.clients) {
            if (c.name.equals(txt_thisclient_name.getText())) {
                c.SendMessage(m);
            }
        }
        String s = "<html><b>" + txt_thisclient_name.getText() + ":" + file.getName() + "</b></html>";
        clientMessagesModel.addElement(s);
        frmFile.setVisible(false);
    }//GEN-LAST:event_btn_sendFileActionPerformed

